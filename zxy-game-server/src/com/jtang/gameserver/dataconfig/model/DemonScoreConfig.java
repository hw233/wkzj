package com.jtang.gameserver.dataconfig.model;

import com.jtang.core.dataconfig.ModelAdapter;
import com.jtang.core.dataconfig.annotation.DataFile;
/**
 * 集众降魔阵营规则配置
 * @author ludd
 *
 */
@DataFile(fileName = "demonScoreConfig")
public class DemonScoreConfig implements ModelAdapter {

	/**
	 * 难度id
	 */
	private int difficultId;
	
	private int scoreNum;
	private int featsRankMin;
	private int featsRankMax;
	
	
	@Override
	public void initialize() {
		
	}
	
	public int getDifficultId() {
		return difficultId;
	}

	public int getScoreNum() {
		return scoreNum;
	}

	public int getFeatsRankMin() {
		return featsRankMin;
	}

	public int getFeatsRankMax() {
		return featsRankMax;
	}
	
	
	

}
