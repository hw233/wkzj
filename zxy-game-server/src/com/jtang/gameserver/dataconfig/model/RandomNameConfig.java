package com.jtang.gameserver.dataconfig.model;

import com.jtang.core.dataconfig.ModelAdapter;
import com.jtang.core.dataconfig.annotation.DataFile;

/**
 * 随机名配置
 * @author vinceruan
 *
 */
@DataFile(fileName = "randomNameConfig")
public class RandomNameConfig implements ModelAdapter {

	private int id;
	private String lastName;
	private String maleFirstName1;
	private String maleFirstName2;
	private String femaleFirstName1;
	private String femaleFirstName2;

	@Override
	public void initialize() {
	}
	
	public int getId() {
		return id;
	}

	public String getLastName() {
		return lastName;
	}

	public String getMaleFirstName1() {
		return maleFirstName1;
	}

	public String getMaleFirstName2() {
		return maleFirstName2;
	}

	public String getFemaleFirstName1() {
		return femaleFirstName1;
	}

	public String getFemaleFirstName2() {
		return femaleFirstName2;
	}
}
