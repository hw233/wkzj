package com.jtang.gameserver.module.app.model.extension.rulevo;

public class ExtBuyHeroVO {

	/**
	 * 仙人id
	 */
	public int heroId;
	
	/**
	 * 可够买次数
	 */
	public int buyNum;
	
	/**
	 * 数量
	 */
	public int num;
	
	/**
	 * 消耗点券公式
	 */
	public String costTicket;
	
	public ExtBuyHeroVO(String[] str) {
		this.heroId = Integer.valueOf(str[0]);
		this.buyNum = Integer.valueOf(str[1]);
		this.num = Integer.valueOf(str[2]);
		this.costTicket = str[3];
	}
	
}
