package com.jtang.gameserver.module.goods.type;

/**
 * 物品大分类
 * @author 0x737263
 *
 */
public interface GoodsType {	

	/**
	 * 一般物品  (通常是一些直接点击用的)
	 * <pre>
	 * {@code BaseSubType}
	 * </pre>
	 */
	int BASE = 1;
	
	/**
	 * 道具物品   各种石头，不能直接点击使用的
	 * <pre>
	 * {@code PropSubType}
	 * </pre>
	 */
	int PROP_BASE = 2;
	
	/**
	 * 碎片     各种碎片（武防饰)
	 * <pre>
	 * {@code PieceSubType}
	 * </pre>
	 */
	int PIECE = 3;
	
	/**
	 * 宝箱     金银铜
	 * <pre>
	 * {@code TreasureBoxSubType}
	 * </pre>
	 */
	int TREASURE_BOX = 4;
	
	/**
	 * 礼包     vip,新手等各种礼包换多个物品的
	 */
	int GIFT_BAG = 5;
	
	/**
	 * 货币     金币、点券
	 */
	int CURRENCY = 6;
	
}
