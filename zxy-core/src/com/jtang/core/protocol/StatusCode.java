package com.jtang.core.protocol;

/**
 * 响应消息的状态码
 * 
 * @author 0x737263
 * 
 */
public interface StatusCode {
		
	/**
	 * 成功
	 */
	short SUCCESS = 0;
	
	/**
	 * 无返回结果
	 */
	short NO_RESULTS = 1;
		
	/**
	 * 数据值内容错误
	 */
	short DATA_VALUE_ERROR = 2;
	
	/**
	 * 没有权限
	 */
	short NO_RIGHT = 3;
	
	/**
	 * 数据包验证错误
	 */
	short DATA_VALIDATE_ERROR = 4;
	
	/**
	 * 角色未登陆
	 */
	short ACTOR_NOT_LOGIN = 5;
	
	/**
	 * 点券不足(请统一使用该状态码)
	 */
	short TICKET_NOT_ENOUGH = 6;
	
	/**
	 * 服务器停机维护中
	 */
	short MAINTAIN_SERVER = 7;
	
	/**
	 * 断开连接.客户端与服务端的版本不匹配
	 */
	short SERVER_VERSION_ERROR = 8;
	
	/**
	 * 断开连接. 被防火墙屏蔽(异常数据发包，或洪水攻击)
	 */
	short FIREWALL_BLOCK = 9;
	
	/**
	 * 断开连接.已达到最大连接数
	 */
	short MAX_CLIENT_LIMIE = 10;
	
	/**
	 * 已经被封号
	 */
	short ACTOR_DISABLED = 11;
	
	/**
	 * 操作异常
	 */
	short OPERATION_ERROR = 12;
	/**
	 * 模块未找到
	 */
	short MODULE_NOT_FOUND = 13;
	/**
	 * 模块未开放
	 */
	short MODULE_NOT_OPEN = 14;
	
}