package com.jtang.worldserver.module.crossbattle.type;

/**
 * 战斗胜负结果
 * @author 0x737263
 *
 */
public enum CrossBattleResult {

	/**
	 * 无记录
	 */
	NONE(0),
	
	/**
	 * 失败
	 */
	FAIL(1),
	
	/**
	 * 平局
	 */
	DRAW(2),

	/**
	 * 胜利
	 */
	WIN(3);
	
	private int id;
	
	private CrossBattleResult(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
}
